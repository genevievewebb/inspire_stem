
#A list is acollection of items.
items = ["pen,","ruler" , "laptop" , "set"]
print(items)

#append(used to add items into an existing list)
items.append("phone")
print("\n")
print(items)

#index
print(items.index("laptop"))
print("\n")

#pop(removes an item from a list)
items.pop()
print("\n")
print(items)

#index
#count

#sort(arranges the in alphabetical order)
items.sort()
print("\n")
print(items)

#reverse(arranges the items in the opposite order)
items.reverse()
print("\n")
print(items)

























































